public class Switch {
    public static void main(String[] args) {
        int a = 0;
        switch (a) {
            case 0:
                String aa = "hello";
                System.out.println(aa.length());
                System.out.println(00);
//                break;
            case 1:
                System.out.println(11);
                break;
            case 2:
                System.out.println(22);
            default:
                System.out.println("default");
        }
    }
}
